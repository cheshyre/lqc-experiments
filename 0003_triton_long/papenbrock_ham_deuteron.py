    # Initialize storage
    pools_uni = []
    coeffs, ops = [], []

    # Process lines
    for line in lines:
        line = line.strip()

        if line == "---":
            # End of a block, create Hamiltonian
            if coeffs and ops:
                ops = [qml.pauli.string_to_pauli_word(op) for op in ops]
                H = qml.Hamiltonian(coeffs, ops)
